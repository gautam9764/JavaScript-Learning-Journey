
console.log("increment and Decrement Operations in JavaScript");

let num = 10;// Initial value

console.log("Initial Value of num:", num ); // Outputs 10
console.log("Post-increment (num++): " + (num++)); // Outputs 10, then num becomes 11
console.log("Value of num after Post-increment:", num ); // Outputs 11

num = 10; // Resetting num to 10

console.log("Pre-increment (++num): " + (++num)); // num becomes 11, then outputs 11
console.log("Value of num after Pre-increment:", num ); // Outputs 11

num = 10; // Resetting num to 10

console.log("Post-decrement (num--): " + (num--)); // Outputs 10, then num becomes 9
console.log("Value of num after Post-decrement:", num ); // Outputs 9

num = 10; // Resetting num to 10

console.log("Pre-decrement (--num): " + (--num)); // num becomes 9, then outputs 9
console.log("Value of num after Pre-decrement:", num ); // Outputs 9


